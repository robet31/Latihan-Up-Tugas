# Materi Variabel
# Variabel adalah tempat menyimpan nilai. Contoh:
nama = "Putra"
umur = 26
print(f"Nama: {nama}, Umur: {umur}")

# Tipe Data
# Tipe data di Python bisa berupa:
# - Integer (bilangan bulat)    
# - Float (bilangan desimal)
# - String (teks)
# - Boolean (True/False)

# Contoh Tipe Data
bilangan_bulat = 10          # Integer
bilangan_desimal = 10.5      # Float
teks = "Belajar Python"      # String
status = True                 # Boolean

print(f"Integer: {bilangan_bulat}, Float: {bilangan_desimal}, String: {teks}, Boolean: {status}")

# Operator Aritmatika
# Operator aritmatika digunakan untuk melakukan operasi matematika.
# Contoh operator: +, -, *, /, // (pembagian bulat), % (modulus), ** (pangkat)

a = 15
b = 4

# Operasi Aritmatika
print(f"Penjumlahan: {a + b}")           # 19
print(f"Pengurangan: {a - b}")          # 11
print(f"Perkalian: {a * b}")             # 60
print(f"Pembagian: {a / b}")             # 3.75
print(f"Pembagian Bulat: {a // b}")      # 3
print(f"Modulus: {a % b}")                # 3
print(f"Pangkat: {a ** b}")               # 625

# Operator Logika
# Operator logika digunakan untuk melakukan operasi logika.
# Contoh operator: and, or, not

x = True
y = False

print(f"x and y: {x and y}")              # False
print(f"x or y: {x or y}")                # True
print(f"not x: {not x}")                  # False
